package javadevelopmentDay01;
//Enum is a special class
//We need Enums because inside the Enums, we can store "Public", "Static" and "Final" variables.
public enum Enum01 {
    //In Enum, all variables should be uppercase.
    JANUARY, FEBRUARY, MARCH, APRIL, MAY, JUNE, JULY, AUGUST, SEPTEMBER, OCTOBER, NOVEMBER, DECEMBER
    //We don't use ";" at the end of the code in Enum.
}
/*
    Differences between Enums and Classes
    1)Variables
    2)You cannot create objects by using Enum, but you can create objects from the Classes
    3)Enums cannot extend to ant class, but they can implement interfaces.
    Whereas you can extend to any class form a class, and you can implement any interface from a  class.
    We use Enums for the specifying purpose. E num makes your code "public", "Static" and "final" automatically.
 */



